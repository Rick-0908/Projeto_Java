package Atividades_Listas_Senai.Modularização.CalculadoraSimples;

public class Calculadora {
    double numeroA = 0;
    double numeroB = 0;

    public double soma() {
        return numeroA + numeroB;
    }

    public double subtracao() {
        return numeroA - numeroB;
    }

    public double multiplicacao() {
        return numeroA * numeroB;
    }

    public double divisao() {
        return numeroA / numeroB;
    }
}

